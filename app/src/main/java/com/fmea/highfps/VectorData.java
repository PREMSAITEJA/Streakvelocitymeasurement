package com.fmea.highfps;

public class VectorData {
    public float x, y, u, v, magnitude, angle;

    public VectorData(float x, float y, float u, float v, float magnitude, float angle) {
        this.x = x;
        this.y = y;
        this.u = u;
        this.v = v;
        this.magnitude = magnitude;
        this.angle = angle;
    }
}